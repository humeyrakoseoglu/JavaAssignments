package Question12;

public class SporMusabakasi {
    private String musabakaGunu;
    private String musabakaSaati;

    public SporMusabakasi(String musabakaGunu, String musabakaSaati) {
        this.musabakaGunu = musabakaGunu;
        this.musabakaSaati = musabakaSaati;
    }

    public String getMusabakaGunu() {
        return musabakaGunu;
    }

    public void setMusabakaGunu(String musabakaGunu) {
        this.musabakaGunu = musabakaGunu;
    }

    public String getMusabakaSaati() {
        return musabakaSaati;
    }

    public void setMusabakaSaati(String musabakaSaati) {
        this.musabakaSaati = musabakaSaati;
    }
}
