package Question4;

public class Cikolata
{
    private double sutOrani;
    private double kakaoOrani;

    public Cikolata(double sutOrani, double kakaoOrani) {
        this.sutOrani = sutOrani;
        this.kakaoOrani = kakaoOrani;
    }

    public double getSutOrani() {
        return sutOrani;
    }

    public void setSutOrani(double sutOrani) {
        this.sutOrani = sutOrani;
    }

    public double getKakaoOrani() {
        return kakaoOrani;
    }

    public void setKakaoOrani(double kakaoOrani) {
        this.kakaoOrani = kakaoOrani;
    }
}
